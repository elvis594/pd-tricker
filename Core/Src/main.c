/**
  * @brief  The application entry point.
  * @retval int
  */
int voltage_levels[] = {5, 9, 12,20};
int current_index = 0;  // 初始 5V（数组索引 0）
void adjust_voltage() {
	char key = key_scan();
	switch (key) {
		case 'A': // 增加电压
			if (current_index < 3) {
				current_index++;
				set_voltage(voltage_levels[current_index]);
			}
			break;
		case 'B': // 减少电压
			if (current_index > 0) {
				current_index--;
				set_voltage(voltage_levels[current_index]);
			}
			break;
		default:
			break;
	}
}

